var int = 5;
console.log(typeof(int)); // Number: integer
console.log(int); // 5

var floatingPoint = 1.1;
console.log(typeof(int)); // Number: Floating Point
console.log(floatingPoint); // 1.1

var booleanType = false;
console.log(typeof(booleanType)); // Boolean
console.log(booleanType); // false

var stringType = 'Welcome to JavaScript';
console.log(typeof(stringType)); // String
console.log(stringType); // Welcome to JavaScript

var nullable = null;
console.log(typeof(nullable)); // Object
console.log(nullable); // null

var notDefined;
console.log(typeof(notDefined)); // undefined
console.log(notDefined); // undefined